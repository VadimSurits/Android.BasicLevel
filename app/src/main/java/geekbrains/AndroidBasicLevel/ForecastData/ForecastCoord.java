package geekbrains.AndroidBasicLevel.ForecastData;

public class ForecastCoord {
    private float lat;
    private float lon;

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }
}

